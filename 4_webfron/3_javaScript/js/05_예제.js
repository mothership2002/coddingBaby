let result;

function plus(num1,num2){
    result = num1+num2;
    document.getElementById("result").innerHTML = result;
}
function minus(num1,num2){
    result = num1-num2;
    document.getElementById("result").innerHTML = result;
}
function multiplication(num1,num2){
    result = num1*num2;
    document.getElementById("result").innerHTML = result;
}
function division(num1,num2){
    result = num1/num2;
    document.getElementById("result").innerHTML = result;
}
function modular(num1,num2){
    result = num1%num2;
    document.getElementById("result").innerHTML = result;
}


